import Foundation
import SwiftUI

extension Color {
    public static var beautifulPink: Color {
        return Color(UIColor(red: 0.98, green: 0.36, blue: 0.28, alpha: 1.00))
    }
    
    public static var veryGrayWhite: Color {
        return Color(UIColor(red: 0.93, green: 0.93, blue: 0.93, alpha: 1.00))
    }
}
